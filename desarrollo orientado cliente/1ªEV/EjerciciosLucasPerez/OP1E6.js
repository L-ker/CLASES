let numero1 = prompt("Introduce el primer numero");
let numero2 = prompt("Introduce el segundo numero");

if (numero1 > numero2) {
    console.log("El primer numero es mayor que el segundo");   
} else {
    console.log("El segundo numero es mayor que el segundo");
}

if (numero1 > 0) {
    console.log("El primer numero es positivio");
} else {
    console.log("El primer numero es negativo");
}

if (numero2 > 0) {
    console.log("El segundo numero es positivo");
} else {
    console.log("El segundo numero es negativo");
}